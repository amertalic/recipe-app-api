"""
Calculator functions
"""


def add(x, y):
    """
    Add x and y and return result.
    :param x: int
    :param y: int
    :return: int
    """
    return x + y


def subtract(x, y):
    """
    Subtract x from y return result.
    :param x: int
    :param y: int
    :return: int
    """

    return y - x
